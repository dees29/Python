def rectangle(color):
    t.begin_fill()
    t.fillcolor(color)
    for i in range(2):
        t.fd(300)
        t.right(90)
        t.fd(100)
        t.right(90)
    t.end_fill()

import turtle
t=turtle.Turtle()
t.up()
t.pensize(4)
t.goto(0,-300)
t.down()
t.goto(0,300)
rectangle("#FF9933")
t.goto(0,200)
t.fd(150)
t.color("#000088")
t.circle(-50)
t.setheading(270)
t.fd(50)
t.setheading(0)
for i in range(24):
    t.fd(45)
    t.bk(45)
    t.left(15)
t.setheading(90)
t.fd(50)
t.setheading(0)
t.color("black")
t.fd(150)
t.right(90)
t.fd(100)
t.right(90)
t.fd(300)
t.right(90)
t.fd(100)
t.right(90)
